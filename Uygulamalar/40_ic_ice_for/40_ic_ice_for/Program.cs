using System;

class Icice_for
{
    static void Main()
    {
        double a, b;

        for (a = 1; a <= 2; a++)
        {
            Console.WriteLine("for a= " + a);

            for (b = 1; b <= 2; b++)
            {
                Console.WriteLine("for b= " + b);

            }
        }

        Console.ReadLine();
    }
}