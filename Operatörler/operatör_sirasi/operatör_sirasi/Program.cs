using System;

class Operatör_sirasi
{
    static void Main()
    {
        int i = 3 * 5 / 7;
        Console.WriteLine(i);

        int j = 3 * (5 / 7);
        Console.WriteLine(j);

        int k = 3 / 5 * 7;
        Console.WriteLine(k);

        int m = 5 / 7 * 3;
        Console.WriteLine(m);
    }
}